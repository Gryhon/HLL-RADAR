/**
 * Generate default strong point positions for a map based on spawn orientation.
 * HLL has 5 sectors (each 2 grid cells wide) with one strong point per sector.
 * Strong points are at the center of each sector along the axis of play.
 */
export const getDefaultStrongPoints = (
  config: MapConfig
): { name: string; x: number; y: number; r?: number }[] => {
  if (config.strongPoints) return config.strongPoints;
  if (!config.spawns) return [];

  const isHorizontal =
    config.spawns.allies === "left" || config.spawns.allies === "right";
  const alliesFirst =
    config.spawns.allies === "left" || config.spawns.allies === "top";

  // 5 sector centers: 10%, 30%, 50%, 70%, 90% of map range
  const positions = [0.1, 0.3, 0.5, 0.7, 0.9];
  const { minX, maxX, minY, maxY } = config.bounds;
  const rangeX = maxX - minX;
  const rangeY = maxY - minY;

  const labels = alliesFirst
    ? ["SP 1", "SP 2", "SP 3", "SP 4", "SP 5"]
    : ["SP 5", "SP 4", "SP 3", "SP 2", "SP 1"];

  return positions.map((p, i) => ({
    name: labels[i],
    x: isHorizontal ? minX + p * rangeX : minX + 0.5 * rangeX,
    y: isHorizontal ? minY + 0.5 * rangeY : minY + p * rangeY,
  }));
};

export const getMapConfig = (mapName: string): MapConfig | undefined => {
  const lower = mapName.toLowerCase();
  // Direct match
  if (MAP_CONFIGS[lower]) return MAP_CONFIGS[lower];
  // Strip time-of-day suffixes (night, dawn, dusk, day) — same map, different lighting
  const stripped = lower
    .replace(/\s*(night|dawn|dusk|day)$/, "")
    .replace(/\s+/g, "");
  return MAP_CONFIGS[stripped];
};
